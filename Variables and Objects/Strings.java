public class Strings {

	public static void main(String[] args) {

		
		String text = "hello";
		// String is not a primitive data type but a class, in this case a type
		// of object that can hold text

		String empty = " ";
		String identity = "humanoid";
		String welcome = text + empty + identity;
		// here is an example of concatenating strings
		System.out.println(welcome);
		
		System.out.println("hello" + " " + "humanoid");
		//different method of concatenation 
		
		int age = 55;
		
		System.out.println("my age is " + age);
		// concatenating a string
		

	}

}
