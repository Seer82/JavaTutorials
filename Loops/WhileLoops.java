public class WhileLoops {

	public static void main(String[] args) {

		int num = 0;

		while (num < 5)

		{
			System.out.println("howdy " + num);
			// while value of the integer is less than 30 print the string
			// howdy, if the value exceeds 30 stop the loop.
			num++;
			// increment the value of num by 1 every time the loop executes.
		}

	}
}
